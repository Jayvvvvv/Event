package com.example.event;

public class Form {
    public String fullname, phone_no, email, function_date, no_of_guest, no_of_room, function_type, function_time;

    public Form(String fullname, String phone_no, String email, String function_date, String no_of_guest, String no_of_room, String function_type, String function_time) {
        this.fullname = fullname;
        this.phone_no = phone_no;
        this.email = email;
        this.function_date = function_date;
        this.no_of_guest = no_of_guest;
        this.no_of_room = no_of_room;
        this.function_type = function_type;
        this.function_time = function_time;
    }
}
